package programmingSet1;
// A Java Program to display results of arithmetic operations on two numbers
public class ArithmeticOperations {
	public static void main(String [] args) {
		System.out.println("Main Starts");
		System.out.println("Two numbers are 100 and 123");
		System.out.print(" Addition Result = ");
		System.out.print( 100+123 );
		System.out.print(" Subtraction Result = ");
		System.out.print( 100-123 );
		System.out.print(" Multiplication Result = ");
		System.out.print( 100*123 );
		System.out.print(" Division  Result = ");
		System.out.print( 100/123 );
		System.out.println(" Main Ends");
	}

}

package programmingSet1;
//Java Program to print message "Hello World, Welcome To Java SE!!!" using 
	//print(), println() and printf() statement.

public class HelloJavaSE {
	

		public static void main(String []args) {
			System.out.println("Main Method Starts");
			System.out.println("Message using print statement");
			System.out.print("Hello World , Welcome To Java SE !!!");
			System.out.println();//create a new blank line 
			System.out.println("Message using println statement");
			System.out.println("Hello World , Welcome To Java SE !!!");
			System.out.println("Message using printf statement");
			System.out.printf("Hello World , Welcome To Java SE !!! %n Message ends%n");
			System.out.println("Main Method Ends");
			
		}

	}




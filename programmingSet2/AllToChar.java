package programmingSet2;
//A JAVA program to convert all primitive data types into char data type
public class AllToChar {
	public static void main(String [] args) {
		System.out.println("MAIN STARTS");
		byte b = 123;
		short s = 233;
		int n =1900;
		long l = 8765;
		float f = 543.6576f;
		double d = 765432.87543;
		System.out.println("Byte b = "+b);
		System.out.println("Byte to char= "+(char)b);
		System.out.println("----------------------------");
		
		System.out.println("Short s = "+s);
		System.out.println("Short to char= "+(char)s);
		System.out.println("----------------------------");
		
		System.out.println("int n = "+n);
		System.out.println("int to char= "+(char)n);
		System.out.println("----------------------------");
		
		System.out.println("long l = "+l);
		System.out.println("long to char= "+(char)l);
		System.out.println("----------------------------");
		System.out.println("float f = "+b);
		System.out.println("float to char= "+(char)f);
		System.out.println("----------------------------");
		
		System.out.println("double d = "+d);
		System.out.println("double to char= "+(char)d);
		System.out.println("----------------------------");
		System.out.println("MAIN STARTS");
		
	}

}

package programmingSet2;
// A JAVA program to convert char data to all other primitive data types
public class CharToAll {
	public static void main(String [] args) {
		System.out.println("Main starts");
		char ch = 'u';
		System.out.println("CHAR DATA TO ALL OTHER PRIMITIVE DATA: ");
		System.out.println("Char data ch ="+ch);		
		System.out.println("Char to byte = "+ (byte)ch);
		System.out.println("Char to short = "+ (short)ch);
		System.out.println("Char to int = "+ (int)ch);
		System.out.println("Char to long = "+ (long)ch);
		System.out.println("Char to float = "+ (float)ch);
		System.out.println("Char to double = "+ (double)ch);
		System.out.println("Main Ends");
	}

}

		System.out.println("ASCII VALUE OF I = "+(int)'I');
		System.out.println("ASCII VALUE OF J = "+(int)'J');
		System.out.println("ASCII VALUE OF K = "+(int)'K');
		System.out.println("ASCII VALUE OF L = "+(int)'L');
		System.out.println("ASCII VALUE OF M = "+(int)'M');
		System.out.println("ASCII VALUE OF N = "+(int)'N');
		System.out.println("ASCII VALUE OF O = "+(int)'O');
		System.out.println("ASCII VALUE OF P = "+(int)'P');
		System.out.println("ASCII VALUE OF Q = "+(int)'Q');
		System.out.println("ASCII VALUE OF R = "+(int)'R');
		System.out.println("ASCII VALUE OF S = "+(int)'S');
		System.out.println("ASCII VALUE OF T = "+(int)'T');
		System.out.println("ASCII VALUE OF U = "+(int)'U');
		System.out.println("ASCII VALUE OF V = "+(int)'V');
		System.out.println("ASCII VALUE OF W = "+(int)'W');
		System.out.println("ASCII VALUE OF X = "+(int)'X');
		System.out.println("ASCII VALUE OF Y = "+(int)'Y');
		System.out.println("ASCII VALUE OF Z = "+(int)'Z');
		System.out.println("Main Ends");
